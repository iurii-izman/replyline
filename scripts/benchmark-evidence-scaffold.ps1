param(
  [string]$OutputDir = "",
  [string]$Note = ""
)

$ErrorActionPreference = "Stop"

$root = Split-Path -Parent $PSScriptRoot
$runtimeDir = Join-Path $root "reports\runtime"
if (-not (Test-Path $runtimeDir)) {
  throw "Runtime reports directory not found: $runtimeDir"
}

if ([string]::IsNullOrWhiteSpace($OutputDir)) {
  $OutputDir = Join-Path $root "reports\runtime"
}

New-Item -ItemType Directory -Force -Path $OutputDir | Out-Null

$timestamp = Get-Date -Format "yyyyMMdd-HHmmss"
$jsonPath = Join-Path $OutputDir "benchmark-evidence-$timestamp.json"
$mdPath = Join-Path $OutputDir "benchmark-evidence-$timestamp.md"

$pkg = Get-Content -Raw -Path (Join-Path $root "package.json") | ConvertFrom-Json
$tauriTomlPath = Join-Path $root "src-tauri\Cargo.toml"
$cargoVersion = $null
if (Test-Path $tauriTomlPath) {
  $tomlLines = Get-Content -Path $tauriTomlPath
  $versionLine = $tomlLines | Where-Object { $_ -match '^version\s*=' } | Select-Object -First 1
  if ($versionLine) {
    $cargoVersion = (($versionLine -split "=", 2)[1]).Trim().Trim('"')
  }
}

$artifactFiles = Get-ChildItem -Path $runtimeDir -File |
  Where-Object { $_.Extension -in @(".json", ".md") } |
  Sort-Object LastWriteTimeUtc -Descending

$selected = @()
$patterns = @(
  "first-latency-report.json",
  "latency-comparison.json",
  "duration-comparison.json",
  "*-live-comparison.json",
  "latency-comparison.md",
  "duration-comparison.md",
  "*-live-comparison.md"
)

foreach ($pattern in $patterns) {
  $matched = $artifactFiles | Where-Object { $_.Name -like $pattern } | Select-Object -First 1
  if ($matched) {
    $selected += $matched
  }
}

$selected = @($selected | Sort-Object FullName -Unique)

function Get-SourceTypeNote {
  param([string]$FileName)
  if ($FileName -like "*live-comparison*") { return "live-source" }
  if ($FileName -like "*duration-comparison*") { return "tts/manual-duration-sweep" }
  if ($FileName -like "*latency-comparison*") { return "tts-benchmark-matrix" }
  if ($FileName -like "*first-latency-report*") { return "tts-single-runtime-probe" }
  return "manual-or-unknown"
}

$reportRefs = @(
  $selected | ForEach-Object {
    [ordered]@{
      file = $_.Name
      path = $_.FullName
      lastWriteTimeUtc = $_.LastWriteTimeUtc.ToString("s")
      bytes = $_.Length
      sourceType = Get-SourceTypeNote -FileName $_.Name
    }
  }
)

$hasMeasuredCandidate = @($reportRefs | Where-Object { $_.file -like "*.json" }).Count -gt 0
$status = if ($hasMeasuredCandidate) { "measured" } else { "pending verification" }
$statusNote = if ($hasMeasuredCandidate) {
  "Structured runtime JSON artifact(s) found locally; still workstation-scoped."
} else {
  "No structured runtime JSON artifact selected; keep claims pending verification."
}

$artifact = [ordered]@{
  generatedAtLocal = (Get-Date).ToString("s")
  lane = "benchmark-evidence-scaffold"
  build = [ordered]@{
    npmPackageVersion = $pkg.version
    rustPackageVersion = $cargoVersion
  }
  evidenceStatus = $status
  evidenceStatusNote = $statusNote
  reportsUsed = $reportRefs
  sourceTypeNotes = @(
    "tts: synthetic source through runtime probe speech.",
    "live-source: operator-provided external audio session.",
    "manual: any curated or hand-copied notes not generated by probe scripts."
  )
  operatorNote = $Note
  honesty = [ordered]@{
    proves = @(
      "Identifies which local runtime artifacts back current benchmark discussion.",
      "Captures timestamp/version context for alpha handoff notes."
    )
    doesNotProve = @(
      "Cross-machine reproducibility.",
      "Product-level quality beyond listed artifacts."
    )
  }
}

$artifact | ConvertTo-Json -Depth 8 | Set-Content -Path $jsonPath -Encoding utf8

$md = @()
$md += "# Benchmark evidence scaffold"
$md += ""
$md += "- generated at: $($artifact.generatedAtLocal)"
$md += "- npm package version: $($artifact.build.npmPackageVersion)"
$md += "- rust package version: $($artifact.build.rustPackageVersion)"
$md += "- evidence status: $($artifact.evidenceStatus)"
$md += "- status note: $($artifact.evidenceStatusNote)"
$md += ""
$md += "## Reports used"
$md += ""
if ($reportRefs.Count -eq 0) {
  $md += "- none selected from reports/runtime"
} else {
  foreach ($item in $reportRefs) {
    $md += "- $($item.file) (`$($item.sourceType)`, updated $($item.lastWriteTimeUtc) UTC)"
  }
}
$md += ""
$md += "## Source type notes"
$md += ""
foreach ($line in $artifact.sourceTypeNotes) {
  $md += "- $line"
}
$md += ""
$md += "## Honesty"
$md += ""
$md += "- proves:"
foreach ($line in $artifact.honesty.proves) {
  $md += "  - $line"
}
$md += "- does not prove:"
foreach ($line in $artifact.honesty.doesNotProve) {
  $md += "  - $line"
}

$md -join "`n" | Set-Content -Path $mdPath -Encoding utf8

Write-Host "Saved benchmark evidence scaffold:" -ForegroundColor Green
Write-Host "  $jsonPath"
Write-Host "  $mdPath"
